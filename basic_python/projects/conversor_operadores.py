# CONVERSOR DE DOLARES A PESOS COLOMBIANOS //////////////////////////

dollars = input("Ingrese la cantidad de dólares: ")
dollars = float(dollars)
pesos_valor = 0.00025
pesos = dollars / pesos_valor
pesos = round(pesos, 2)
pesos = str(pesos)
print("Tiene $" + pesos + " pesos colombianos 💛💙❤ ")


